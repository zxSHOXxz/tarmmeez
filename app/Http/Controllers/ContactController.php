    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validator = Validator($request->all(), [
            'name' => 'required|string|max:100',
        ]);


        if ($validator->fails()) {

            return response()->json(['icon' => 'error', 'title' => $validator->getMessageBag()->first()], 400);
        } else {


            $contacts = new Contact();
            $contacts->name = $request->get('name');
            $contacts->email = $request->get('email');
            $contacts->phone = $request->get('phone');
            $contacts->company_name = $request->get('company_name');
            $contacts->inquire_about = $request->get('inquire_about');
            $contacts->project_summary = $request->get('project_summary');
            
            

            $isSaved = $contacts->save();
            return response()->json(['icon' => 'success', 'title' => 'contact created successfully'], $isSaved ? 201 : 400);
        }

        

    }

