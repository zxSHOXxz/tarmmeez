    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validator = Validator($request->all(), [
            'name' => 'required|string|max:100',
        ]);


        if ($validator->fails()) {

            return response()->json(['icon' => 'error', 'title' => $validator->getMessageBag()->first()], 400);
        } else {


            $consultations = new Consultation();
            $consultations->name = $request->get('name');
            $consultations->email = $request->get('email');
            $consultations->phone = $request->get('phone');
            $consultations->service_id = $request->get('service_id');
            $consultations->message = $request->get('message');
            
            

            $isSaved = $consultations->save();
            return response()->json(['icon' => 'success', 'title' => 'Consultation created successfully'], $isSaved ? 201 : 400);
        }

        

    }

